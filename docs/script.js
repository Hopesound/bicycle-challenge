function projectPositionToLatLng(position) {
  const [x, y] = position;
  const lng =
    mapGeoBounds.west + (x / 320) * (mapGeoBounds.east - mapGeoBounds.west);
  const lat =
    mapGeoBounds.north - (y / 220) * (mapGeoBounds.north - mapGeoBounds.south);

  return { lat, lng };
}

