function derivePlaces() {
  const sorted = [...places]
    .filter((place) => !hiddenPlaceNames.has(place.name))
    .sort((a, b) => collator.compare(a.name, b.name));

  sorted.forEach((place, index) => {
    place.alphaOrder = index + 1;
    place.imageUrl =
      placeImages[place.name] ||
      (place.aliases || []).map((alias) => placeImages[alias]).find(Boolean) ||
      '';
    place.mapKeyword =
      place.mapKeyword ||
      placeMapKeywords[place.name] ||
      (place.aliases || []).map((alias) => placeMapKeywords[alias]).find(Boolean) ||
      '';
    place.position =
      placePositions[place.name] ||
      (place.aliases || []).map((alias) => placePositions[alias]).find(Boolean) ||
      [160, 110];
    place.latLng = projectPositionToLatLng(place.position);
    place.searchText = [
      place.name,
      ...(place.aliases || []),
      place.district,
      place.tagline,
      place.intro,
      place.nearbyText,
      place.nearby.join(' ')
    ]
      .join(' ')
      .toLowerCase();
  });

  Object.keys(themeMeta)
    .filter((key) => key !== 'all')
    .forEach((themeKey) => {
      const themePlaces = sorted.filter((place) => place.theme === themeKey);
      themePlaces.forEach((place, index) => {
        place.themeOrder = index + 1;
        place.themeCount = themePlaces.length;
      });
    });

  return sorted;
}

